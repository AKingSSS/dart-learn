void main() {
  for (int i = 0; i <= 10; i++) {
    print("i = " + i.toString());
  }
  print("------------------------------------");
  int j = 1;
  while (j <= 10) {
    print("j = " + j.toString());
    j++;
  }
  print("------------------------------------");
  int k = 1;
  do{
    print("k = " + k.toString());
    k++;
  } while(k <= 10);
}
